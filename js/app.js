function myFunction() {
  var x = document.getElementById("myDIV");
  var y = document.getElementById("myIMG");
  if (x.style.display === "none") {
    x.style.display = "block";
    y.src = "asset/minus.svg";
  } else {
    x.style.display = "none";
    y.src = "asset/cross.svg";
  }
}

